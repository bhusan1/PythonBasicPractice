user_age = input("Enter your age: ")
years = int(user_age)
months = years*12
print(f"Yours age, {years}, is equal to {months} months.")

days = years *365
print(f"Yours age, {years}, is equal to {days} days.")

seconds = days*86400
print(f"Yours age, {years}, is equal to {seconds} seconds.")


