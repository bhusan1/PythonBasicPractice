# " == " is used to compare for boolean
# comparisons: ==, !=, >, <, >=, <=

print (5 == 5) #True
print (5 != 5) #False
print (5 < 5) #False
print (5 > 5) #False
print (5 <= 5) #True
print (5 >= 5) #True

#comparing lists
